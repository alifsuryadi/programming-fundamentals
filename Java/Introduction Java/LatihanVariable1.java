package Materi_Wajib_1_Pengenalan_Java;
public class LatihanVariable1 {
    public static void main(String[] args) {
     int a, b, temp;
     a = 15;
     b = 27;
     System.out.println("Hasil untuk print pertama ini adalah: " + " "+ a+ " "+ b);
     temp = a;
     a = b;
     b = temp;
     System.out.println("Hasil untuk print kedua ini adalah: "+" "+ a+" "+ b);
    }
    }
    
