package Materi_Wajib_1_Pengenalan_Java;

public class StudiKasus1 {

    public static void main(String []args){
        int a = 19;

        /*println = menempatkan kursor di awal garis
        printf  = menulis format spesifik seperti %d, %s, dll
        %o = nilai oktal
        %x = nilai heksadesimal*/

        System.out.printf("Nilai Oktal dari 19        :%o\n",a);
        System.out.printf("Nilai Heksadesimal dari 19 :%x\n",a);

    }

}